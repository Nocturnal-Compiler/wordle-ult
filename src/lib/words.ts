// Get today's word based on a fixed start date
export function getDailyWord(): string {
  const startDate = new Date('2024-01-01')
  const today = new Date()
  const diffTime = Math.abs(today.getTime() - startDate.getTime())
  const diffDays = Math.floor(diffTime / (1000 * 60 * 60 * 24))
  return SOLUTION_WORDS[diffDays % SOLUTION_WORDS.length]
}

// Get a random word for unlimited mode
export function getRandomWord(): string {
  return SOLUTION_WORDS[Math.floor(Math.random() * SOLUTION_WORDS.length)]
}

// Check if a word is valid
export function isValidWord(word: string): boolean {
  return VALID_GUESSES.has(word.toLowerCase())
}

// Get today's date as string for comparison
export function getTodayString(): string {
  const today = new Date()
  return `${today.getFullYear()}-${String(today.getMonth() + 1).padStart(2, '0')}-${String(today.getDate()).padStart(2, '0')}`
}
